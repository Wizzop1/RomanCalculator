package com.example.digitconverter.converter;

import java.util.TreeMap;

public interface Converter {

    TreeMap<Integer, String> arabicToRoman = new TreeMap<>() {{
        put(1000, "M");
        put(900, "CM");
        put(500, "D");
        put(400, "CD");
        put(100, "C");
        put(90, "XC");
        put(50, "L");
        put(40, "XL");
        put(10, "X");
        put(9, "IX");
        put(5, "V");
        put(4, "IV");
        put(1, "I");
    }};

    TreeMap<Character, Integer> romanToArabic = new TreeMap<>() {{
        put('I', 1);
        put('V', 5);
        put('X', 10);
        put('L', 50);
        put('C', 100);
        put('D', 500);
        put('M', 1000);
    }};

    String toRoman(int number);

    int toArabic(String s);
}
